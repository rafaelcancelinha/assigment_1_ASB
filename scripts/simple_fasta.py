def simplificar_fasta(inp, out="simplified.fasta"):
    with open(inp, "r") as f_in, open(out, "w") as f_out:
        for line in f_in:
            if line.startswith(">"):
                parts = line.strip()[1:].split()
                accession = parts[0].split(".")[0]

                if parts[1] == "UNVERIFIED:":
                    genus = parts[2]
                    species = parts[3]
                else:
                    genus = parts[1]
                    species = parts[2]

                strain = accession_to_strain.get(accession, "NoStrain")
                f_out.write(f">{genus}_{species}_{strain}\n")
            else:
                f_out.write(line)
    return out

